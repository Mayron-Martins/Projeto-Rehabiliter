    public void setarConfigsInciciais(){
        radioHorUnico.setSelected(true);
        painelHorUnico.setVisible(true);
        painelHorDiversos.setVisible(false);
        
        for(int linha=1; linha<campoHorariosI.size(); linha++){
            campoHorariosI.get(linha).setVisible(false);
            campoHorariosF.get(linha).setVisible(false);
        }
        trocaBotoes(false);
    }
    
    public void paineisHorarios(boolean unico){
        painelHorUnico.setVisible(unico);
        painelHorDiversos.setVisible(!unico);
        radioHorDiversos.setSelected(!unico);
        if(radioHorDiversos.isSelected()){
            visibilidadeHorariosPosTroca();
        }
    }
    
    private void visibilidadeHorariosPosTroca(){
        int cont=1;
        for(JCheckBox caixa : diasSemana){
            if(caixa.isSelected()){
                visibleHorario(caixa, campoHorariosI.get(cont), campoHorariosF.get(cont));
            }
            cont++;
        }
    }
    
    private void visibleHorario(JCheckBox caixaSelecao, JFormattedTextField campo, JFormattedTextField campo2){
        if(radioHorDiversos.isSelected()&&caixaSelecao.isSelected()){
            campo.setVisible(true);
            campo2.setVisible(true);
        }
        else{
            campo.setVisible(false);
            campo2.setVisible(false);
        }
    }
    
    public void trocaBotoes(boolean editar){
        botaoEditar.setVisible(!editar);
        botaoConcluir.setVisible(editar);
        enableCampos(editar);
    }

    private void enableCampos(boolean enable){
        for(JCheckBox caixa : diasSemana){
            caixa.setEnabled(enable);
        }
        
        for(JFormattedTextField campo : campoHorariosI){
            campo.setEditable(enable);
        }
        
        for(JFormattedTextField campo : campoHorariosF){
            campo.setEditable(enable);
        }
        
        campoNome.setEditable(enable);
        campoCodHorarios.setEditable(enable);
        campoQuantPresente.setEditable(false);
        campoCodHorarios.setEditable(false);
        radioHorUnico.setEnabled(enable);
        radioHorDiversos.setEnabled(enable);
    }
    
    
