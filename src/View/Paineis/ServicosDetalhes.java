    public void formaPagamento(boolean unica){
        painelFormaUnica.setVisible(unica);
        painelFormaDiversas.setVisible(!unica);
        radioFormaPag2.setSelected(!unica);
    }
    
    private void grupoBotoes(){
        ButtonGroup grupoFormas = new ButtonGroup();
        grupoFormas.add(radioFormaPag1);
        grupoFormas.add(radioFormaPag2);
    }
    
    public void setarConfigsInciciais(){
        radioFormaPag1.setSelected(true);
        
        comboPeriodo.setEnabled(true);
        campoOutroTipo.setText("Outro");
        campoOutroTipo.setEnabled(false);
        comboDias.setEnabled(false);
        painelNovoTipo.setVisible(false);
        formaPagamento(true);
       
        trocaBotoes(false);
    }
    
    public void trocaBotoes(boolean editar){
        botaoEditar.setVisible(!editar);
        botaoConcluir.setVisible(editar);
        enableCampos(editar);
    }

    private void enableCampos(boolean enable){
        nomeServico.setEditable(enable);
        comboPeriodo.setEnabled(enable);
        campoOutroTipo.setEditable(enable);
        comboDias.setEnabled(enable);
        campoDias.setEditable(false);
        campoDiasNovo.setEditable(enable);
        radioFormaPag1.setEnabled(enable);
        radioFormaPag2.setEnabled(enable);
        valorUnico.setEditable(enable);
        valorBoleto.setEditable(enable);
        valorDinheiro.setEditable(enable);
        valorCredito.setEditable(enable);
        valorDebito.setEditable(enable);
    }
    

